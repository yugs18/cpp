#include <iostream>
using namespace std;
int main()
{
/*
Primitive Types
- int for integers(whole numbers)
- float and double for decimal values, with double providing more precision
- char for single characters
- bool for Boolean values(true or false)
*/

int a = 12;
float b = 12.21;
double c = 12.321;
char d = 'Y';
bool e = false;
cout << "int: " << a << "\nfloat: " << b << "\ndouble: " << c << "\nchar: " << d << "\nbool: " << e << "\n";

/*
Derived Types
- Arrays, pointers, references and functions types

User-defined Types
- Structures(struct), classes(class), and enumerations(enum)
*/
}

